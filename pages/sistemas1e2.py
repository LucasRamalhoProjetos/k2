st.title("Sistema 1 e Sistema 2 ")

# Texto principal
st.write("""
Os conceitos de Sistema 1 e Sistema 2 vêm do psicólogo Daniel Kahneman, apresentados no livro Rápido e Devagar: Duas Formas de Pensar. Ele usa essa ideia para descrever como o cérebro humano processa informações.
         
Sistema 1 (Rápido, Intuitivo e Automático)
Funciona de forma automática e quase sem esforço.
É responsável por decisões rápidas, instintivas e emocionais.
Não exige atenção consciente.
Atua com base na experiência e em associações.
Exemplo: Reconhecer um rosto conhecido, dirigir um carro em uma estrada vazia ou responder rapidamente a perguntas simples como "2 + 2".
Sistema 2 (Lento, Analítico e Esforçado)
Envolve pensamento consciente, lógico e deliberado.
Exige atenção e esforço mental.
É acionado quando lidamos com problemas complexos ou situações novas.
Exemplo: Resolver uma equação matemática difícil, tomar decisões estratégicas ou planejar o futuro.
Como eles se relacionam:
O Sistema 1 é o piloto automático do cérebro, funcionando o tempo todo. Já o Sistema 2 entra em ação quando o Sistema 1 não consegue resolver algo ou quando a situação exige mais concentração.

Por exemplo, quando você está dirigindo e alguém atravessa na frente do carro, o Sistema 1 reage automaticamente freando. Mas se você precisar calcular o tempo estimado para chegar ao destino levando em conta o trânsito, o Sistema 2 assume.

Resumo:
Sistema 1: Rápido, automático, emocional, instintivo.
Sistema 2: Lento, lógico, racional, consciente.
Essa teoria explica por que às vezes tomamos decisões impulsivas sem pensar muito, enquanto em outras situações precisamos parar e refletir.
""")
