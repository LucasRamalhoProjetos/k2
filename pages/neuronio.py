# Texto principal
st.write("""
Um neurônio é a célula fundamental do sistema nervoso, responsável por transmitir informações pelo corpo. Ele é essencial para todas as funções do cérebro, desde movimentos simples até pensamentos complexos.

O que é um Neurônio?
Os neurônios são células especializadas que transmitem sinais elétricos e químicos. Eles formam redes de comunicação que permitem que o cérebro e o corpo funcionem em conjunto.

Estrutura do Neurônio
Um neurônio tem três partes principais:

Corpo Celular (Soma):

Contém o núcleo e a maior parte da maquinaria celular.
É onde a energia é produzida e as informações são processadas.
Dendritos:

Pequenas ramificações que recebem sinais de outros neurônios.
Quanto mais dendritos, mais conexões o neurônio pode ter.
Axônio:

Uma extensão longa que transmite sinais elétricos para outros neurônios, músculos ou glândulas.
O axônio pode ser coberto por uma bainha de mielina (uma camada isolante) que ajuda a acelerar a transmissão do sinal.
Terminal Axônico (ou Botão Terminal):

Ponta do axônio, onde os sinais elétricos são convertidos em sinais químicos para se comunicarem com outros neurônios através das sinapses.
Como um Neurônio Funciona?
O processo de comunicação do neurônio acontece em duas etapas:

Sinal Elétrico (Impulso Nervoso):

Quando um neurônio recebe um estímulo forte o suficiente, ele gera um sinal elétrico chamado potencial de ação.
Esse sinal percorre o axônio até o terminal axônico.
Sinal Químico (Sinapse):

No terminal axônico, o sinal elétrico desencadeia a liberação de substâncias químicas chamadas neurotransmissores.
Os neurotransmissores atravessam a sinapse (espaço entre dois neurônios) e se ligam aos receptores nos dendritos do próximo neurônio, passando a informação adiante.
Importância dos Neurônios
Os neurônios são essenciais para:

Controle de movimentos
Processamento de informações sensoriais (visão, audição, tato)
Memória e aprendizado
Emoções e pensamentos
Reflexos automáticos
Comunicação entre diferentes partes do corpo
Curiosidade: Redes Neurais Artificiais
A estrutura e o funcionamento dos neurônios servem de inspiração para as redes neurais artificiais na inteligência artificial, como a Julia que você está desenvolvendo. As redes neurais artificiais tentam imitar a maneira como os neurônios biológicos processam informações e aprendem com experiências.

Conclusão
Os neurônios são como os "fios" que conectam o sistema nervoso, formando uma rede incrivelmente complexa que permite todas as atividades do corpo e da mente. Quanto mais conexões entre os neurônios, mais poderoso é o processamento cerebral.
""")
